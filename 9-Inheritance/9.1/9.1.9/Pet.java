public class Pet extends Animal
{
    private String name;
    private String size;
    
    public void setName(String _name)
    {
        this.name = _name;
    }
    public String getName()
    {
        return this.name;
    }
    public void setSize(String _size)
    {
        this.size = _size;
    }
    public String getSize()
    {
        return this.size;
    }    
   
}
