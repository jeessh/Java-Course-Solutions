public class MyAge
{
    public static void main(String[] args)
    {
        int age = 17;

        System.out.print("My current age is: ");
        System.out.println(age);
        
        age++;
        
        System.out.print("My age next year will be: ");
        System.out.println(age);
        
        age--;
        
        System.out.print("My current age is: ");
        System.out.print(age);
    }
}

//My current age is: 14
//My age next year will be: 15
//My current age is: 14
